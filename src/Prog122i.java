public class Prog122i {
    public static void main(String[] args){
        int cb = -25;
        int num = cb;
        double cbd = -25;
        System.out.printf("Number\t\t\tCube\t\t\tCube Root\n");
        while (num <= 25){
            cb = num*num*num;
            cbd = Math.cbrt(num);
            num++;
            System.out.printf("%d\t\t\t\t%d\t\t\t\t%.4f\n", num - 1, cb, cbd);
        }
    }
}
